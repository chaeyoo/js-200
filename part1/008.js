// * 원시타입
// - 값이 변수에 할당될 때 메모리 상에 고정된 크기로 저장, 변수가 직접 값을 보관
// => 고정된 공간에 보관된 원시 타입 값은 변경 불가능한 값
// null, undefined, Number, Boolean, String, Symbol


// * 참조타입
// - 변수에 고정된 크기를 저장하지 않고, 값이 메모리 주소를 참조한다
// - 객체는 속성들의 집합
// - 집합 내부에는 순서, 크기는 고정되지 않음
// - 참조 타입은 값의 메모리 주소를 변수에 할당하여 값을 참조