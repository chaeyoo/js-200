// 012 조건문 배우기 - switch

var subject = 'JavaScript';

switch (subject) {
    case 'C':
        console.log('C입니다')
        break;
    case 'JavaScript':
        console.log('JavaScript입니다');
        break;
    case 'Python':
        console.log('Python입니다');
        break;
    default:
        console.log('이젠 초보가 아닙니다');
        break;
}